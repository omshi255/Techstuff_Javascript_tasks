
// Take 2 numbers and 2 strings and perform addition 
// operation on them in same sequence. 


let N = 5;

console.log("Initial N =", N);

// --- Pre-increment (++N) ---
console.log("Pre-increment (++N):", ++N); 
console.log("Now N =", N);

// --- Post-increment (N++) ---
console.log("Post-increment (N++):", N++); 
console.log("Now N =", N);

// --- Pre-decrement (--N) ---
console.log("Pre-decrement (--N):", --N);
console.log("Now N =", N);

// --- Post-decrement (N--) ---
console.log("Post-decrement (N--):", N--); 
console.log("Now N =", N);

// --- Division by zero ---
console.log("10 / 0 =", 10 / 0);   // Infinity
console.log("-10 / 0 =", -10 / 0); // -Infinity
console.log("0 / 0 =", 0 / 0);     // NaN

// --- Remainder (sign follows dividend) ---
console.log("5 % 2 =", 5 % 2);   
console.log("-5 % 2 =", -5 % 2);  
console.log("5 % -2 =", 5 % -2);   
console.log("-5 % -2 =", -5 % -2);                                                                                                                                                                                                                                                                             
